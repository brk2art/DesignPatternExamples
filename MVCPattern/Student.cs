namespace MVCPattern
{
    public class Student
    {
        private string rollNo;
        private string name;

        public string getRollNo()
        {
            return rollNo;
        }

        public void setRollNo(string rollNo)
        {
            this.rollNo = rollNo;
        }

        public string getName()
        {
            return name;
        }

        public void setName(string name)
        {
            this.name = name;
        }
    }
}
